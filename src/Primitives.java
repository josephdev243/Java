public class Primitives {
    public static void main(String[] args) {
        // Integer type
        int rollNo = 64;
        System.out.println("Roll Number: " + rollNo);

        // Character type
        char letter = 'r';
        System.out.println("Letter: " + letter);

        // Floating-point number (Single precision)
        float marks = 98.67f;
        System.out.println("Marks: " + marks);

        // Double precision floating-point number
        double largeDecimalNumbers = 4567654.4567;
        System.out.println("Large Decimal Number: " + largeDecimalNumbers);

        // Long Integer (64-bit)
        long largeInteger = 34567893456765473L;
        System.out.println("Large Integer: " + largeInteger);

        // Boolean type
        boolean check = true;
        System.out.println("Boolean Value: " + check);

        // Short integer (16-bit)
        short smallNumber = 32000;
        System.out.println("Short Number: " + smallNumber);

        // Byte (8-bit) - Smallest integer type
        byte byteNumber = 127;
        System.out.println("Byte Number: " + byteNumber);

        // Performing arithmetic operations
        int sum = rollNo + 10;
        System.out.println("Roll Number after addition: " + sum);

        double multiplication = marks * 2.5;
        System.out.println("Marks multiplied: " + multiplication);

    }
}
